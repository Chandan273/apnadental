                            <h2 class="text-uppercase">What type of procedures does a periodontist perform?

                            </h2>
                            <p>A periodontist can perform a wide range of periodontal treatments, including periodontal
                                maintenance, scaling and root planing, and several types of periodontal surgery.
                            </p>
                        </div>


                        <div>
                            <h2 class="text-uppercase">Periodontal maintenance
                            </h2>
                            <p>Periodontal maintenance refers to a type of teeth cleaning. It’s similar to a regular
                                cleaning at your dentist’s office. But in addition to cleaning your teeth, your
                                periodontist or hygienist checks your gum health and measures the pockets around your
                                teeth. (When you lose bone around your teeth, the pockets get deeper.) Many people who
                                need periodontal maintenance should have these cleanings every three to four months.
                                Your periodontist can recommend a cleaning schedule that’s right for you.
                            </p>

                        </div>

                        <div>
                            <h2 class="text-uppercase">Scaling and root planing (periodontal cleaning)
                            </h2>
                            <p>“Scaling and root planing (SRP)” is another term for a deep dental cleaning. The main
                                difference is that SRP requires local anesthesia to numb your gums. This allows your
                                hygienist to clean deep underneath your gum line, where harmful bacteria hide. They’ll
                                also smooth the surfaces of your teeth roots to discourage plaque and bacteria from
                                building up.

                            </p>
                            <p>Periodontists typically recommend scaling and root planing for people with early-stage
                                (mild) gum disease.</p>
                        </div>

                        <!-- end  -->



                        <div>
                            <h2 class="text-uppercase">Porcelain veneers
                            </h2>
                            <p>Made of medical grade ceramic, porcelain veneers are thin, strong shells that
                                adhere to
                                the front surfaces of your teeth. Like dental bonding, veneers can conceal a
                                wide range
                                of cosmetic flaws, including chips, cracks and discoloration.

                            </p>
                            <p>There are many types of veneers, including traditional and minimal prep. All
                                veneers
                                require at least some removal of natural enamel, which means they could be
                                reversible
                                depending on which kind you choose. In most cases, porcelain veneers need
                                replacing
                                every 10 years or so.
                            </p>
                        </div>



                        <div>
                            <h2 class="text-uppercase">Tooth contouring

                            </h2>
                            <p>This procedure removes small amounts of enamel to change the shape of your
                                teeth. Because
                                you only have so much natural enamel, there’s a limit to how much tooth
                                structure your
                                dentist can remove. Your dentist can complete tooth contouring during a
                                single office
                                visit.
                            </p>

                        </div>

                        <div>
                            <h2 class="text-uppercase">Gum contouring


                            </h2>
                            <p>Some people are born with excess gum tissue. When this happens, it can make
                                your smile
                                appear “gummy” or unbalanced. During gum contouring, your dentist removes
                                excess gum
                                tissue and reshapes your gum line for a more symmetrical, balanced
                                appearance.
                            </p>

                        </div>


                        <div>
                            <h2 class="text-uppercase">Can restorative dental procedures also offer cosmetic
                                benefits?
                            </h2>
                            <p>The procedures outlined above are strictly cosmetic in nature. But sometimes,
                                restorative
                                dentistry treatments, which repair teeth, can also improve the appearance of
                                your smile.
                                For example, if you repair a cracked tooth with a dental crown, it not only
                                improves the
                                health and function of your smile, but also its appearance.
                            </p>
                            <p>Similarly, dental braces aim to improve your bite and your overall oral
                                health. But
                                braces also improve the aesthetics of your smile dramatically.</p>
