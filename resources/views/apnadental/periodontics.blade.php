                        <h2 class="text-uppercase">Why would you see a periodontist?

                        </h2>
                        <p>There are several reasons why someone would need to see a periodontist. Some
                            people only need to see a periodontist temporarily. In other words, they may
                            need to undergo treatment that’s outside the scope of practice for a general
                            dentist. For example, maybe you need a gum graft, bone graft or frenectomy. A
                            periodontist can perform your surgical treatment, then refer you back to your
                            general dentist when your recovery is complete.
                        </p>
                        <p>Other people may need to see a periodontist long term. This includes people who
                            are prone to gum disease. Those who are genetically predisposed to gum disease
                            usually need periodontal cleanings in addition to routine cleanings at their
                            dentist’s office. In these cases, your periodontist will work closely alongside
                            of your general dentist. You’ll continue to receive care from both of them in
                            order to maintain optimal oral health.</p>


                        <div class="my-4">
                            <h2 class="text-uppercase">What exactly does a periodontist do?
                            </h2>
                            <p>A periodontist treats oral health issues that affect your periodontium (the
                                tissues around your teeth). This includes your:

                            </p>

                            <ul class="list-group gap-2 list-group-numbered">
                                <li class="list-group-item">Gingiva: Your gums. </li>
                                <li class="list-group-item">Periodontal ligament: A group of connective
                                    tissue fibers that attach your teeth to your jawbone.</li>
                                <li class="list-group-item">Cementum: A hard layer of calcified tissue that
                                    covers your teeth roots.</li>
                                <li class="list-group-item">Alveolar bone: The part of your jawbone that
                                    has tooth sockets.</li>

                            </ul>


                        </div>
