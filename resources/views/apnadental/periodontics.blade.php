                        <h2 class="text-uppercase">What is periodontics?
                        </h2>
                        <p>Periodontics is a dental specialty. The word “periodontics” comes from
                            two
                            Greek words: “peri,” which means “around” and “odont,” which
                            means“tooth.”
                            So, the field of periodontics treats conditions that affect the tissues
                            “around your teeth,” such as bone loss, gum recession and periodontal
                            (gum)
                            disease.

                        </p>
                    </div>

                    <div>
                        <h2 class="text-uppercase">What’s the difference between a dentist and a
                            periodontist?
                        </h2>
                        <p>A general or family dentist focuses on preventative and restorative
                            treatments. For instance, if you need a routine dental cleaning, you see
                            your dentist. A dentist also fills cavities and places restorations such
                            as dental crowns or dental bridges. In the U.S., a general dentist must
                            graduate from a four-year dental school before receiving their license
                            to practice.
                        </p>
                        <p>A periodontist is a gum specialist. They focus on treating conditions
                            that affect the tissues that support your teeth (such as your gums and
                            jawbone). In addition to graduating from a four-year dental school, a
                            periodontist receives three years of additional training before earning
                            their license to practice periodontics in the U.S.</p>
                    </div>


